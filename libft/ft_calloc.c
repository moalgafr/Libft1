#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*res;

	if (count > INT_MAX || size > INT_MAX || (size * count) > INT_MAX)
		return (NULL);
	res = malloc(count * size);
	if (!res)
		return (NULL);
	ft_bzero(res, count * size);
	return (res);
}
